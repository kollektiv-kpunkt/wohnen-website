                <p class="absatz"><sup>6</sup>Um den Kauf oder Bau von neuen Wohnungen zu ermöglichen, kann der Stadtrat zu Gunsten von gemeinnützigen Wohnbauträgerinnen – namentlich Genossenschaften sowie den stadteigenen Stiftungen – in eigener Kompetenz Bürgschaften eingehen. Zudem kann er den stadteigenen Stiftungen in eigener Kompetenz Darlehen gewähren.</p>
