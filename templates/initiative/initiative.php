        <div id="initiative" style="padding-top: calc(var(--navHeight) + 1.5rem)">
            <h4 style="margin-top: 0">Initiativtext</h4>
            <div id="initiativtext" style="margin-bottom: 1rem;">
                <p id="initiativtext-title">Kantonale Volksinitiative «Bezahlbare Wohnungen schützen. Leerkündigungen stoppen (Wohn-Initiative)»</p>
                <p id="initiativtext-subtitle">I. Das Gesetz über die Wohnbau- und Wohneigentumsförderung vom 7. Juni 2004 (LS 841) wird wie folgt geändert:</p>
                <p>V. Wohnbauförderung und Wohnschutzmassnahmen der Gemeinden<br>
                Wohnschutzmassnahmen</p>
                <div class="kapitel">
                    <p class="kapitel-title">a. im Allgemeinen</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 a.</p>
                        <p class="absatz"><sup>1</sup>Zum Erhalt und zur Sicherung von Mietwohnungen, die für breite Bevölkerungskreise finanziell tragbar sind, können die Gemeinden selbständig Vorschriften zum Wohnschutz erlassen, insbesondere</p>
                        <p class="litera">a. eine Bewilligungspflicht für Abbrüche, Umbauten und Renovationen sowie Zweckänderungen</p>
                        <p class="litera">b. Beschränkungen für die Umwandlung von Mietwohnungen in Stockwerkeigentum. </p>
                        <p class="absatz"><sup>2</sup>Die Bewilligung kann mit Auflagen zur Begrenzung der Mietzinse verbunden werden. Die Auflagen sind zeitlich zu beschränken.</p>
                        <p class="absatz"><sup>3</sup>Die Anwendung der Vorschriften und Auflagen zum Wohnschutz ist an das Vorliegen eines Wohnungsmangels auf dem Gemeindegebiet (Leerwohnungsbestand von weniger als 1,5 %) zu knüpfen.</p>
                        <p class="absatz"><sup>4</sup>Die Gemeinden regeln die Wohnschutzmassnahmen in einem Gemeindeerlass. Die Wohnschutzmassnahmen gelten nicht für Wohnbauträger, die der Gemeinnützigkeit und dem Prinzip kostendeckender Mieten ohne Gewinnabsichten verpflichtet sind.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">b. Verfahren</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 b.</p>
                        <p class="absatz"><sup>1</sup>Bei Bauvorhaben in Gemeinden, die Massnahmen zum Wohnschutz erlassen haben, wird die Anwendbarkeit der Vorschriften zum Wohnschutz öffentlich bekannt gemacht.</p>
                        <p class="absatz"><sup>2</sup>Bei der Umwandlung in Stockwerkeigentum stellt das Grundbuchamt der Bewilligungsbehörde die Grundbuchanmeldung und den Rechtgrundausweis zu. Es sistiert das Eintragungsverfahren bis zum Vorliegen eines rechtskräftigen Entscheids.</p>
                        <p class="absatz"><sup>3</sup>Die Auflagen zur Begrenzung der Mietzinse sind im Grundbuch als Anmerkung gemäss Art. 962 ZGB einzutragen.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">b. Rechtsschutz</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 c.</p>
                        <p class="absatz"><sup>1</sup>Die Anordnungen betreffend Wohnschutz werden zusammen mit der Baubewilligung eröffnet. Rekursinstanz ist das Baurekursgericht. Die Rechtsmittelinstanzen überprüfen die Angemessenheit der Auflagen zur Begrenzung der Mietzinse.</p>
                        <p class="absatz"><sup>2</sup>Zum Rekurs und zur Beschwerde berechtigt ist, wer durch die angefochtene Anordnung berührt ist und ein schutzwürdiges Interesse an ihrer Aufhebung oder Änderung hat, insbesondere </p>
                        <p class="litera">a. Mieterinnen und Mieter, deren Mietobjekt Gegenstand des Entscheides ist,</p>
                        <p class="litera">b. Gesamtkantonal tätige Verbände, die sich seit wenigstens zehn Jahren im Kanton statutengemäss dem Mieterschutz widmen.</p>
                        <p class="absatz"><sup>3</sup>Die Berechtigung zum Rekurs und zur Beschwerde ist vom Fortbestand des Mietverhältnisses unabhängig.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">§ 14 a wird zu § 14 d.</p>
                </div>
