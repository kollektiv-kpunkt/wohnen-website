            <div id="initiativtext" style="margin-bottom: 1rem;">
                <p id="initiativtext-title">Kantonale Volksinitiative «Mehr bezahlbare Wohnungen im Kanton Zürich»</p>
                <p id="initiativtext-subtitle">Kommunales Vorkaufsrecht</p>
                <div class="kapitel">
                    <p class="kapitel-title">a. Allgemeines</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 b.</p>
                        <p class="absatz"><sup>1</sup>Zur Förderung des gemeinnützigen und preisgünstigen Wohnens sowie der Bereitstellung von Alterswohnungen können die Gemeinden in einem Gemeindeerlass ein Vorkaufsrecht an Grundstücken vorsehen und alle erforderlichen Bestimmungen erlassen.</p>
                        <p class="absatz"><sup>2</sup>Das Vorkaufsrecht kann für Eigentumsübertragungen von Grundstücken in Bauzonen vorgesehen werden, die mehrheitlich der Wohnnutzung dienen oder auf denen eine mehrheitliche Wohnnutzung planungs- und baurechtlich zulässig ist.</p>
                        <p class="absatz"><sup>3</sup>Es geht vertraglichen Vorkaufsrechten vor und ist zum gesetzlichen Vorkaufsrecht gemäss Art. 682 ZGB subsidiär.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">b. Ausnahmen</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 c.</p>
                        <p class="absatz"><sup>1</sup>Vom Vorkaufsrecht ausgenommen sind Eigentumsübertragungen</p>
                            <p class="litera">a. zwischen Familienangehörigen, die in gerader Linie oder in der Seitenlinie bis und mit dem vierten Grad verwandt oder verschwägert sind,</p>
                            <p class="litera">b. wegen Eigenbedarfs der erwerbenden Person,</p>
                            <p class="litera">c. an gemeinnützige Wohnbauträger.</p>
                        <p class="absatz"><sup>2</sup>Die Gemeinden können weitere Ausnahmen vorsehen, insbesondere können sie Grundstücke bis zu einer bestimmten Grösse vom Vorkaufsrecht ausnehmen.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">c. Ausübungspreis und Aufwendungsersatz</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 d.</p>
                        <p class="absatz"><sup>1</sup>Die Gemeinde kann das Vorkaufsrecht zu den Bedingungen der erwerbenden Person ausüben.</p>
                        <p class="absatz"><sup>2</sup>Sie vergütet den Parteien die angemessenen Aufwendungen und aufgelaufenen Zinsen.</p>
                    </div>
                </div>
                <div class="kapitel">
                    <p class="kapitel-title">d. Verfahren</p>
                    <div class="artikel">
                        <p class="artikel-no">§ 14 e.</p>
                        <p class="absatz"><sup>1</sup>Lässt sich ein Vorkaufsfall nicht ausschliessen, trägt das Grundbuchamt die Grundbuchanmeldung in das Tagebuch ein und sistiert das weitere Eintragungsverfahren. Es stellt der Gemeinde die Grundbuchanmeldung und den Rechtsgrundausweis zu.</p>
                        <p class="absatz"><sup>2</sup>Beabsichtigt die Gemeinde, das Vorkaufsrecht auszuüben, setzt der Gemeindevorstand die Parteien und das Grundbuchamt innert einer Verwirkungsfrist von 60 Tagen schriftlich und unter Angabe der Gründe davon in Kenntnis. Zugleich setzt der Gemeindevorstand den Parteien eine Frist von 20 Tagen zur Stellungnahme. Unterbleibt die Absichtserklärung, kann der Verkauf in das Hauptbuch eingetragen werden.</p>
                        <p class="absatz"><sup>3</sup>Innert einer Ordnungsfrist von 60 Tagen ab Eingang der Stellungnahmen kann der Gemeindevorstand das Vorkaufsrecht mit schriftlichem und begründetem Entscheid definitiv ausüben.</p>
                        <p class="absatz"><sup>4</sup>Eine allfällige Ausgabenbewilligung durch ein anderes Organ ist vorbehalten und kann nach Ablauf der Frist erfolgen.</p>
